public class Team {
    private String teamName;
    private Team[] team;

    public Team(Team[] team){
        this.teamName = teamName;
        this.team = team;
    }

    public Team[] getTeam() {
        return team;
    }

    public String getTeamName() {
        return teamName;
    }
}
